import { Client } from 'ssh2';

const conn = new Client();

conn.on('ready', () => {
    console.log('âœ… SSH Conectado ao servidor Supabase');

    // Get all trigger names and their functions
    const cmd = `docker exec $(docker ps -q -f name=supabase_db | head -1) psql -U postgres -d postgres -c "SELECT tgname, proname FROM pg_trigger t JOIN pg_proc p ON t.tgfoid = p.oid WHERE tgrelid = 'auth.users'::regclass ORDER BY tgname;"`;

    conn.exec(cmd, (err, stream) => {
        if (err) {
            console.error('Erro:', err);
            conn.end();
            return;
        }

        let output = '';
        stream.on('data', (data) => {
            output += data.toString();
        });
        stream.stderr.on('data', (data) => {
            output += data.toString();
        });

        stream.on('close', () => {
            console.log('=== LISTA DE TRIGGERS EM AUTH.USERS ===');
            console.log(output);
            conn.end();
        });
    });
});

conn.on('error', (err) => {
    console.error('Erro SSH:', err.message);
});

